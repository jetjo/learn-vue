<template>
  <div>
    Ref(模版引用)也可以用在一个子组件上,这个时候获取到的ref值分三种情况:
    <ul>
      <li>
        1、如果子组件是以选项式声明的,那么这个ref值就相当于子组件中的this,可以访问子组件的任何状态和方法
      </li>
      <li>
        2、如果子组件是以setup方法调用的组合式方式声明的,和选项式一样,通过ref值也可以访问子组件的所有状态和方法
      </li>
      <li>
        3、如果子组件是以`&lt;script setup
        >`语法糖形式的组合式方式声明的,那么默认子组件的所有状态和方法都是私有的,
        <br />子组件可以使用defineExpose宏方法来决定要暴露哪些状态和方法给父组件.
      </li>
    </ul>
  </div>
</template>
